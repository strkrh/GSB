package fr.be2.gsb_rs;

public class Frais {
    String code = null;
    String Type_Forfait = null;
    String Quantite = null;
    String date = null;

    public String getCode() {
        return code;
    }
    public void setCode(String code) {
        this.code = code;
    }
    public String getType_Forfait() {
        return Type_Forfait;
    }
    public void setType_Forfait(String Type_Forfait) {
        this.Type_Forfait = Type_Forfait;
    }
    public String getQuantite() {
        return Quantite;
    }
    public void setQuantite(String Quantite) {
        this.Quantite = Quantite;
    }
    public String getdate() {
        return date;
    }
    public void setdate(String date) {
        this.date = date;
    }


}


